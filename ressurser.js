// Create tabs using map
const tabHeader = document.querySelector('.tab-header');
resources.map((topic, index) => {
  const tab = document.createElement('span');
  tab.className = 'tab';
  tab.textContent = topic.category;
  tab.addEventListener('click', () => changeTab(index + 1));
  tabHeader.appendChild(tab);
});

// Create tab contents
const tabContents = document.querySelector('.tab-contents');
resources.map((topic, index) => {
  const tabContent = document.createElement('article');
  tabContent.className = 'tab-content';
  tabContent.id = 'tab' + (index + 1);
  tabContent.innerHTML = `<h2>${topic.category}</h2><p>${topic.text}</p><ul>${topic.sources.map(source => `<li><a href="${source.url}" target="_blank">${source.title}</a></li>`).join('')}</ul>`;
  tabContents.appendChild(tabContent);
});


  // Show default tab
  changeTab(1);

  function changeTab(tabNumber) {
    document.querySelectorAll('.tab-content').forEach(content => {
        content.classList.remove('active');
      });
    
      // Hide all tabs
      document.querySelectorAll('.tab').forEach(tab => {
        tab.classList.remove('active');
      });
    
      // Show the selected tab content
      document.getElementById('tab' + tabNumber).classList.add('active');
    
      // Show the selected tab
      document.querySelector('.tab-header').children[tabNumber - 1].classList.add('active');
    }